node_colors = {}
for node in data["nodes"]:
    node_id = node["data"]["id"]
    node_type = node["data"].get("type", "unknown")
    G.add_node(node_id, type=node_type)
    
    # Assign colors based on type
    if node_type == "parent":
        node_colors[node_id] = "red"
    elif node_type == "gene":
        node_colors[node_id] = "blue"
    elif node_type == "transcript":
        node_colors[node_id] = "green"
    elif node_type == "exon":
        node_colors[node_id] = "orange"
    else:
        node_colors[node_id] = "gray"

# Add edges with labels
edge_labels = {}
for edge in data["edges"]:
    source = edge["data"]["source"]
    target = edge["data"]["target"]
    label = edge["data"]["label"]
    G.add_edge(source, target, label=label)
    edge_labels[(source, target)] = label

# Draw graph
plt.figure(figsize=(10, 8))
pos = nx.spring_layout(G, seed=42)
nx.draw(G, pos, with_labels=True, node_color=[node_colors[n] for n in G.nodes], edge_color="black", node_size=3000, font_size=8)
nx.draw_networkx_edge_labels(G, pos, edge_labels=edge_labels, font_size=8)

